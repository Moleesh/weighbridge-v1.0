DROP TABLE IF EXISTS WEIGHING;
CREATE TABLE IF NOT EXISTS WEIGHING (
	SLNO	INTEGER NOT NULL,
	DCNO	VARCHAR ( 20 ),
	DCNODATE	DATE,
	CUSTOMERNAME	VARCHAR ( 100 ),
	DRIVERNAME VARCHAR ( 100 ),
	VEHICLENO	VARCHAR ( 100 ),
	MATERIAL	VARCHAR ( 100 ),
	NOOFBAGS	INTEGER,
	CHARGES	INTEGER,
	GROSSWT	INTEGER,
	GROSSDATE	DATE,
	GROSSTIME	TIME,
	TAREWT INTEGER,
	TAREDATE	DATE,
	TARETIME	TIME,
	BAGDEDUCTION	INTEGER,
	NETWT	INTEGER,
	NETDATE	DATE,
	NETTIME	TIME,
